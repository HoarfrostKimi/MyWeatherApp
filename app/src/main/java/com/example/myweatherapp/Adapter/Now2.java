package com.example.myweatherapp.Adapter;

public class Now2 {
    String weather;
    String felllike;
    String temp;
    public Now2( String weather, String felllike, String temp) {
        this.weather = weather;
        this.felllike = felllike;
        this.temp = temp;
    }



    public String getweather() {
        return weather;
    }

    public String getfelllike() {
        return felllike;
    }

    public String gettemp() {
        return temp;
    }

}
